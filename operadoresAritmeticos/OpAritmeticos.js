let res = 0
export function somar(a,b){
    res = a+b
    return res;
}

export function subtrair(a,b){
    res = a-b
    return res
}

export function multiplicar(a,b){
    res = a*b
    return res
}

export function dividir(a,b){
    res = a/b
    return res
}

export function potencia(a,b){
    res = a**b
    return res
}

export function raizQuadrada(a){
    res = Math.sqrt(a)
    return res
}

export function restoDivisao(a,b){
    res = a%b
    return res
}
